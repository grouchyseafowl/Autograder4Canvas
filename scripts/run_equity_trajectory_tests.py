_INTER_PHASE_PAUSE = 30   # seconds between phases
_CODING_BATCH_SIZE  = 8   # students per batch; model unloads between batches to prevent Metal OOM


def _run_phase_subprocess(phase_id: str, model: str, run_id: str = "1") -> Optional[int]:
    """Run a single phase in an isolated subprocess."""
    import subprocess as sp
    import shutil

    if _phase_done(phase_id):
        log.info("Phase %s already done (flag file exists), skipping.", phase_id)
        return 0

    cmd = [
        sys.executable, __file__,
        "--single-phase", phase_id,
        "--model", model,
        "--run-id", run_id,
    ]

    # On macOS with MLX, wrap in caffeinate to prevent sleep from invalidating
    # the Metal GPU context mid-run (the leading cause of check_error SIGABRT crashes).
    if sys.platform == "darwin" and MODELS.get(model, {}).get("name") == "mlx":
        caffeinate = shutil.which("caffeinate")
        if caffeinate:
            cmd = [caffeinate, "-i"] + cmd
            log.info("Using caffeinate to prevent Metal sleep-induced crashes")

    log.info("Subprocess for phase %s: %s", phase_id, " ".join(cmd))
    timeout = 3600 if phase_id == "OBSERVATIONS" else 7200  # 2h for coding+observations
    result = sp.run(cmd, timeout=timeout)
    return result.returncode


def _run_single_phase(phase_id: str, model: str):
    """Execute one phase in the current process (called from subprocess)."""
    if phase_id in ASSIGNMENT_CONFIGS:
        run_coding_phase(phase_id, model)
    elif phase_id == "OBSERVATIONS":
        run_observations_phase(model)
    else:
        log.error("Unknown phase: %s", phase_id)
        sys.exit(1)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    import argparse
    parser = argparse.ArgumentParser(description="Equity trajectory tests")
    parser.add_argument("--phases", default=",".join(ALL_PHASES),
                        help=f"Comma-separated phases: {','.join(ALL_PHASES)}")
    parser.add_argument("--model", default="gemma12b",
                        help="Model key: gemma12b, gemma27b_cloud")
    parser.add_argument("--observations-only", action="store_true",
                        help="Skip coding passes, evaluate observations only")
    parser.add_argument("--no-subprocess", action="store_true",
                        help="Run all phases in-process (dev/debug only)")
    parser.add_argument("--reset-flags", action="store_true",
                        help="Clear done-flags and re-run all phases from scratch")
    parser.add_argument("--single-phase", default=None,
                        help="(internal) Run a single phase in this process")
    parser.add_argument("--run-id", default="1",
                        help="Unique suffix for COURSE_ID (e.g. P3, P4_clean). "
                             "Prevents get_student_history() from bleeding prior "
                             "run observations into the current run. Each clean "
                             "replication should use a different --run-id.")
    args = parser.parse_args()

    # Override module-level COURSE_ID so all phases in this run use the same
    # isolated course — get_student_history() will only see observations from
    # this run, not from prior test runs sharing EQ_TEST_1.
    global COURSE_ID
    COURSE_ID = f"EQ_TEST_{args.run_id}"

    if args.single_phase:
        _run_single_phase(args.single_phase, args.model)
        return

    if args.reset_flags:
        for phase in ALL_PHASES:
            flag = _flag_path(phase)
            if flag.exists():
                flag.unlink()
                log.info("Reset flag: %s", phase)

    if args.observations_only:
        phases = ["OBSERVATIONS"]
    else:
        phases = [p.strip().upper() for p in args.phases.split(",")]
        for p in phases:
            if p not in ALL_PHASES:
                log.error("Unknown phase: %s (valid: %s)", p, ALL_PHASES)
                sys.exit(1)

    model = args.model
    model_display = MODELS.get(model, {}).get("model", model)

    print(f"\n{'═'*70}")
    print(f"  Equity Trajectory Tests")
    print(f"  Course: {COURSE_NAME}  [id: {COURSE_ID}]")
    print(f"  Corpus: {CORPUS_PATH.name}  ({len(load_corpus()['students'])} students)")
    print(f"  Model:  {model_display}")
    print(f"  Phases: {phases}")
    print(f"  Output: {OUTPUT_DIR}")
    if not args.no_subprocess:
        print(f"  Mode:   subprocess isolation (Metal memory reclaimed per phase)")
    if MODELS.get(model, {}).get("name") == "mlx":
        import shutil as _shutil
        if _shutil.which("caffeinate") and not args.no_subprocess:
            print(f"  Metal:  caffeinate -i auto-applied per phase (sleep protection)")
        else:
            print(f"  NOTE:   Run with 'caffeinate -i' to prevent Metal deadlocks on sleep:")
            print(f"          caffeinate -i python scripts/run_equity_trajectory_tests.py --model {model}")
    print(f"{'═'*70}")

    if not args.no_subprocess:
        _metal_warmup(model)

    phase_results = {}

    for phase_id in phases:
        if args.no_subprocess:
            _run_single_phase(phase_id, model)
            phase_results[phase_id] = "done"
        else:
            try:
                rc = _run_phase_subprocess(phase_id, model, args.run_id)
                phase_results[phase_id] = "OK" if rc == 0 else f"exit {rc}"
            except Exception as e:
                log.error("Phase %s subprocess failed: %s", phase_id, e)
                phase_results[phase_id] = f"FAILED: {e}"

            if phase_id != phases[-1]:
                log.info("Pausing %ds for Metal memory reclamation...",
                         _INTER_PHASE_PAUSE)
                time.sleep(_INTER_PHASE_PAUSE)

    print(f"\n{'='*70}")
    print(f"  ALL PHASES COMPLETE")
    print(f"{'='*70}")
    for phase_id, status in phase_results.items():
        print(f"  Phase {phase_id}: {status}")
    print(f"  Flag dir: {FLAG_DIR}")
    print(f"  Outputs:  {OUTPUT_DIR}")


if __name__ == "__main__":
    main()
