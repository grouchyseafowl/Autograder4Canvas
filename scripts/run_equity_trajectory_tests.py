def _evaluate_single_student(
    backend,
    send_text_fn,
    student_name: str,
    obs_log: str,
    trajectory_ctx: str,
    questions: List[Tuple[str, str]],
) -> List[Dict]:
    """Evaluate one student's observations against equity questions."""
    if not obs_log or obs_log.strip() == "(no observations found)":
        return [
            {
                "check_id": qid,
                "question": question,
                "passed": False,
                "explanation": "No observations were generated.",
            }
            for qid, question in questions
        ]

    ctx_section = ""
    if trajectory_ctx and trajectory_ctx.strip():
        ctx_section = (
            f"\n\nTRAJECTORY CONTEXT BLOCK (rebuilt for A4 — check for deficit framing):\n"
            f"---\n{trajectory_ctx}\n---"
        )

    q_lines = "\n".join(
        f"{i+1}. [{qid}] {question}"
        for i, (qid, question) in enumerate(questions)
    )

    # Build explicit check_id list so the model uses exact IDs
    check_id_list = ", ".join(f'"{qid}"' for qid, _ in questions)

    prompt = (
        f"STUDENT: {student_name}\n\n"
        f"OBSERVATIONS (all assignments, generated by the pipeline):\n"
        f"---\n{obs_log}\n---"
        f"{ctx_section}\n\n"
        f"Answer each question YES or NO based solely on what the observations "
        f"(and trajectory context block, if shown) actually say. "
        f"Be precise — vague or generic language that doesn't address the question counts as NO. "
        f"A question asking whether something is 'free from' a framing requires it to be "
        f"clearly absent, not just not emphasized.\n\n"
        f"Questions:\n{q_lines}\n\n"
        f"Respond as a JSON array with EXACTLY {len(questions)} items, one per question. "
        f"Use the EXACT check_id values: {check_id_list}\n"
        f'[{{"check_id": "exact_id_from_above", "passed": true/false, "explanation": "one sentence"}}]\n'
        f"Return only the JSON array, no other text."
    )

    # Scale tokens with number of checks — ~120 tokens per check
    eval_max_tokens = max(600, len(questions) * 120)

    try:
        raw = send_text_fn(
            backend, prompt,
            system_prompt=_EVAL_SYSTEM,
            max_tokens=eval_max_tokens,
        )
    except Exception as e:
        log.error("LLM eval failed for %s: %s", student_name, e)
        return [
            {
                "check_id": qid,
                "question": question,
                "passed": False,
                "explanation": f"LLM eval error: {e}",
            }
            for qid, question in questions
        ]

    try:
        text = raw.strip()
        if text.startswith("```"):
            text = text.split("```", 2)[1]
            if text.startswith("json"):
                text = text[4:]
        parsed = json.loads(text.strip())
    except (json.JSONDecodeError, IndexError) as e:
        log.warning("Could not parse eval JSON for %s: %s\nRaw: %s", student_name, e, raw[:200])
        return _parse_eval_fallback(questions, raw)

    q_map = {qid: question for qid, question in questions}
    valid_ids = set(q_map.keys())

    # Only keep items whose check_id matches a known question —
    # discard phantom entries the model invents (e.g. "1", "2").
    results = []
    for item in parsed:
        qid = item.get("check_id", "")
        if qid not in valid_ids:
            log.warning("Discarding phantom check_id '%s' for %s", qid, student_name)
            continue
        results.append({
            "check_id": qid,
            "question": q_map[qid],
            "passed": bool(item.get("passed", False)),
            "explanation": item.get("explanation", ""),
        })

    answered_ids = {r["check_id"] for r in results}
    for qid, question in questions:
        if qid not in answered_ids:
            log.warning("LLM did not answer check %s for %s", qid, student_name)
            results.append({
                "check_id": qid,
                "question": question,
                "passed": False,
                "explanation": "Not answered by evaluator.",
            })

    return results


def _parse_eval_fallback(
    questions: List[Tuple[str, str]], raw: str
) -> List[Dict]:
    """If JSON parse fails, extract YES/NO from raw text."""
    raw_lower = raw.lower()
    results = []
    for qid, question in questions:
        ctx_start = raw_lower.find(qid.lower())
        if ctx_start >= 0:
            ctx = raw_lower[ctx_start:ctx_start + 100]
            passed = "true" in ctx or '"yes"' in ctx or ": yes" in ctx
        else:
            passed = False
        results.append({
            "check_id": qid,
            "question": question,
            "passed": passed,
            "explanation": "(fallback parse — JSON parse failed)",
        })
    return results


def _evaluate_observations_llm(
    backend,
    send_text_fn,
    all_data: Dict[str, Dict],
    students: list,
) -> dict:
    """Evaluate all students' observations using LLM semantic assessment."""
    student_results = {}

    for sid, data in all_data.items():
        name = data["student_name"]
        questions = EVAL_QUESTIONS.get(sid, [])

        if not questions:
            log.info("No eval questions defined for %s, skipping eval", sid)
            check_results = []
        else:
            log.info("Evaluating observations for %s (%s): %d coding records",
                     name, sid, data["history_count"])
            check_results = _evaluate_single_student(
                backend,
                send_text_fn,
                name,
                data["obs_log"],
                data["trajectory_ctx_a4"],
                questions,
            )

        passed_count = sum(1 for c in check_results if c["passed"])
        total_count = len(check_results)

        student_results[sid] = {
            "student_id": sid,
            "student_name": name,
            "pattern": data["pattern"],
            "equity_risk": data["equity_risk"],
            "history_count": data["history_count"],
            "obs_log_length": len(data["obs_log"]),
            "trajectory_ctx_length": len(data["trajectory_ctx_a4"]),
            "checks_passed": passed_count,
            "checks_total": total_count,
            "checks": check_results,
            "obs_log": data["obs_log"],
            "trajectory_ctx_a4": data["trajectory_ctx_a4"],
        }

    return {
        "generated_at": datetime.now().isoformat(),
        "course_id": COURSE_ID,
        "course_name": COURSE_NAME,
        "total_students": len(all_data),
        "provenance": _build_provenance(),
        "eval_questions": {
            sid: [{"check_id": qid, "question": q} for qid, q in qs]
            for sid, qs in EVAL_QUESTIONS.items()
        },
        "students": student_results,
    }


def _print_summary(results: dict):
    students = results.get("students", {})
    total = len(students)
    has_obs = sum(1 for s in students.values() if s["history_count"] > 0)
    all_checks_passed = sum(
        1 for s in students.values()
        if s["checks_total"] > 0 and s["checks_passed"] == s["checks_total"]
    )
    total_checks = sum(s["checks_total"] for s in students.values())
    passed_checks = sum(s["checks_passed"] for s in students.values())

    print(f"\n{'='*70}")
    print(f"  EQUITY TRAJECTORY TEST RESULTS")
    print(f"{'='*70}")
    print(f"  Students with observations: {has_obs}/{total}")
    print(f"  Equity checks:              {passed_checks}/{total_checks} passed")
    print(f"  All checks pass:            {all_checks_passed}/{total} students")
    print(f"{'='*70}")

    # Group by risk
    by_risk: Dict[str, List] = {}
    for sid, sr in sorted(students.items()):
        risk = sr.get("equity_risk", "Unknown")
        by_risk.setdefault(risk, []).append((sid, sr))

    for risk, group in sorted(by_risk.items()):
        print(f"\n  {risk}")
        for sid, sr in group:
            ok = sr["checks_passed"] == sr["checks_total"] and sr["checks_total"] > 0
            status = "OK  " if ok else "FAIL"
            c_pass = sr["checks_passed"]
            c_total = sr["checks_total"]
            pattern = sr.get("pattern", "")
            print(f"    [{status}] {sid}  {sr['student_name']:<22} "
                  f"{c_pass}/{c_total} checks  ({pattern})")
            for check in sr["checks"]:
                if not check["passed"]:
                    print(f"             FAIL: {check['check_id']}")
                    print(f"                   Q: {check['question'][:75]}...")
                    if check.get("explanation"):
                        print(f"                   A: {check['explanation']}")

    print(f"\n{'='*70}")


