def run_coding_phase(assignment_id: str, model_key: str):
    """Run InsightsEngine coding for one assignment.

    Processes submissions in batches of _CODING_BATCH_SIZE and performs a full
    MLX model unload between batches.  Each batch creates its own run_id in
    InsightsStore (same course_id / assignment_name), so run_observations_phase
    and get_student_history pick up all codings correctly.
    """
    from settings import load_settings
    from insights.engine import InsightsEngine
    from insights.insights_store import InsightsStore

    corpus = load_corpus()
    submissions = submissions_for_assignment(corpus, assignment_id)
    cfg = ASSIGNMENT_CONFIGS[assignment_id]

    log.info("Coding phase %s: %d submissions", assignment_id, len(submissions))

    user_settings = load_settings()

    batches = [submissions[i:i + _CODING_BATCH_SIZE]
               for i in range(0, len(submissions), _CODING_BATCH_SIZE)]

    last_run_id = None
    for batch_num, batch in enumerate(batches):
        if batch_num > 0:
            # Full model unload between batches: releases weight tensors, sets
            # Metal cache limit to 0 so fragmented buffers are returned to the
            # driver, then restores the limit.  Prevents the cumulative Metal
            # OOM that crashes check_error() mid-phase.
            log.info("Batch %d/%d: unloading model to reclaim Metal memory...",
                     batch_num + 1, len(batches))
            from insights.llm_backend import unload_mlx_model
            unload_mlx_model()

        log.info("Coding phase %s batch %d/%d: %d submissions",
                 assignment_id, batch_num + 1, len(batches), len(batch))
        store = InsightsStore()
        engine = InsightsEngine(api=None, store=store, settings=user_settings)

        run_id = engine.run_from_submissions(
            submissions=batch,
            course_id=COURSE_ID,
            course_name=COURSE_NAME,
            assignment_id=cfg["assignment_id"],
            assignment_name=cfg["assignment_name"],
            model_tier="lightweight",
            teacher_context=cfg["teacher_context"],
            next_week_topic=cfg.get("next_week_topic") or "",
            progress_callback=lambda msg: log.info("  %s", msg),
            stop_after="observations",  # skip themes/outliers/synthesis/feedback
        )

        if run_id is None:
            log.error("Coding phase %s batch %d returned None run_id",
                      assignment_id, batch_num + 1)
            sys.exit(1)

        last_run_id = run_id

    log.info("Coding phase %s complete: last run_id=%s", assignment_id, last_run_id)
    _unload_model()
    _mark_done(assignment_id)


def run_observations_phase(model_key: str) -> dict:
    """
    Retrieve stored observations for each student, rebuild trajectory context
    blocks for A3/A4, and evaluate both for equity issues using LLM assessment.
    """
    from insights.insights_store import InsightsStore
    from insights.trajectory_context import build_trajectory_context
    from insights.llm_backend import BackendConfig, send_text

    corpus = load_corpus()
    students = corpus["students"]

    store = InsightsStore()

    # Build backend config
    model_cfg = MODELS.get(model_key, MODELS["gemma12b"])
    api_key = ""
    if "api_key_fn" in model_cfg:
        api_key = model_cfg["api_key_fn"]()
    backend = BackendConfig(
        name=model_cfg["name"],
        model=model_cfg["model"],
        max_tokens=model_cfg.get("max_tokens", 400),
        temperature=model_cfg.get("temperature", 0.3),
        base_url=model_cfg.get("base_url", ""),
        api_key=api_key,
    )

    # Map assignment_name → run_id for this course
    all_runs = store.get_runs(COURSE_ID)
    runs_by_assignment_name: Dict[str, Dict] = {}
    for run in all_runs:
        aname = run.get("assignment_name", "")
        if aname and aname not in runs_by_assignment_name:
            runs_by_assignment_name[aname] = run

    log.info("Found %d runs for course %s: %s",
             len(all_runs), COURSE_ID,
             list(runs_by_assignment_name.keys()))

    # Verify coding data
    history_counts = {}
    for student in students:
        sid = student["student_id"]
        history = store.get_student_history(sid, COURSE_ID)
        history_counts[sid] = len(history)

    total_coded = sum(history_counts.values())
    log.info("Coding records in store: %d total across %d students",
             total_coded, len(students))

    missing = [sid for sid, cnt in history_counts.items() if cnt == 0]
    if missing:
        log.warning("%d students have 0 coding records: %s", len(missing), missing)

    student_index = {s["student_id"]: s for s in students}

    # Build observation logs and trajectory context blocks per student
    all_data: Dict[str, Dict] = {}
    for student in students:
        sid = student["student_id"]
        name = student["student_name"]

        # All coding records for this student (ordered chronologically)
        history = store.get_student_history(sid, COURSE_ID)

        # Extract observations from coding records
        obs_parts: List[str] = []
        for rec in history:
            coding = rec.get("coding_record") or {}
            if isinstance(coding, str):
                try:
                    coding = json.loads(coding)
                except Exception:
                    coding = {}
            obs = coding.get("observation", "")
            aname = rec.get("assignment_name", "?")
            date = rec.get("started_at", "")
            if obs and obs.strip():
                obs_parts.append(f"[{aname}]:\n{obs.strip()}")
            else:
                obs_parts.append(f"[{aname}]: (no observation generated)")

        obs_log = "\n\n".join(obs_parts) if obs_parts else "(no observations found)"

        # Rebuild trajectory context for A4 to inspect for deficit language.
        # We call build_trajectory_context() directly, passing the A4 run as
        # "current" and A1+A2+A3 records as prior history.
        a4_aname = ASSIGNMENT_CONFIGS["A4"]["assignment_name"]
        a4_run = runs_by_assignment_name.get(a4_aname)
        trajectory_ctx_a4 = ""
        if a4_run:
            # When coding is split into batches, A4 may span multiple run_ids.
            # Use this student's actual A4 run_id (from their history) so that
            # build_trajectory_context excludes the right run.
            a4_run_id = next(
                (r.get("run_id", "") for r in history
                 if r.get("assignment_name") == a4_aname),
                a4_run.get("run_id", ""),  # fallback: first batch's run_id
            )
            # Filter by assignment name rather than exclude_run_id — handles
            # the case where A4 codings are spread across multiple run_ids.
            prior_history = [r for r in history
                             if r.get("assignment_name") != a4_aname]
            if prior_history:
                # Find A4 coding record for current signals
                a4_coding: Dict = {}
                for rec in history:
                    if rec.get("assignment_name") == ASSIGNMENT_CONFIGS["A4"]["assignment_name"]:
                        cr = rec.get("coding_record") or {}
                        if isinstance(cr, str):
                            try:
                                cr = json.loads(cr)
                            except Exception:
                                cr = {}
                        a4_coding = cr
                        break

                # Pull corpus submission for A4 to get submitted_at
                a4_submitted_at = ""
                for sub in student.get("submissions", []):
                    if sub["assignment_id"] == "A4":
                        a4_submitted_at = sub.get("submitted_at", "")
                        break

                try:
                    trajectory_ctx_a4 = build_trajectory_context(
                        store=store,
                        student_id=sid,
                        course_id=COURSE_ID,
                        current_run_id=a4_run_id,
                        current_word_count=a4_coding.get("word_count", 0),
                        current_submitted_at=a4_submitted_at,
                        current_register=a4_coding.get("emotional_register", ""),
                        current_engagement_depth=(
                            (a4_coding.get("engagement_signals") or {}).get("engagement_depth")
                            if isinstance(a4_coding.get("engagement_signals"), dict)
                            else None
                        ),
                        current_unknown_word_rate=float(
                            a4_coding.get("unknown_word_rate", 0.0) or 0.0
                        ),
                        current_linguistic_assets=list(
                            a4_coding.get("linguistic_assets") or []
                        ),
                        current_theme_tags=list(
                            a4_coding.get("theme_tags") or []
                        ),
                        current_personal_connections=list(
                            a4_coding.get("personal_connections") or []
                        ),
                        current_readings_referenced=list(
                            a4_coding.get("readings_referenced") or []
                        ),
                    )
                except Exception as e:
                    log.warning("build_trajectory_context failed for %s: %s", sid, e)
                    trajectory_ctx_a4 = f"(trajectory context build failed: {e})"

        all_data[sid] = {
            "student_id": sid,
            "student_name": name,
            "pattern": student.get("pattern", ""),
            "equity_risk": student.get("equity_risk", ""),
            "obs_log": obs_log,
            "trajectory_ctx_a4": trajectory_ctx_a4,
            "history_count": len(history),
        }

    # LLM evaluation
    log.info("Evaluating observations via LLM...")
    results = _evaluate_observations_llm(backend, send_text, all_data, students)

    _unload_model()

    results["model"] = {
        "key": model_key,
        "name": model_cfg["name"],
        "model_id": model_cfg["model"],
    }

    ts = datetime.now().strftime("%Y-%m-%d_%H%M")
    out_path = OUTPUT_DIR / f"equity_observations_{model_key}_{ts}.json"
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(results, indent=2))
    log.info("Results saved: %s", out_path)

    _print_summary(results)
    _mark_done("OBSERVATIONS")
    return results


